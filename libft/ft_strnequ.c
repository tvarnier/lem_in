#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	int i;

	if (s1 && s2)
	{
		i = 0;
		while (s1[i] && s2[i] && s1[i] == s2[i] && i < (int)n)
			i++;
		return ((!s1[i] && !s2[i]) || i == (int)n ? 1 : 0);
	}
	return (0);
}
